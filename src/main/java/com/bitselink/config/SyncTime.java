package com.bitselink.config;

public class SyncTime {
    public String from;
    public String to;

    public SyncTime() {
        from = "";
        to = "";
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
}
